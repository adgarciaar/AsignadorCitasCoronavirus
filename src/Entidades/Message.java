package Entidades;

import java.io.Serializable;

/**
 *
 * @author adgar
 */
public class Message implements Serializable {
    
    private int codigo;
    private String texto;

    public Message() {
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getTexto() {
        return texto;
    }

    public void setTexto(String texto) {
        this.texto = texto;
    }
    
}
